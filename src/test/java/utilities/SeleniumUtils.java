package utilities;

import org.openqa.selenium.WebElement;
import org.openqa.selenium.support.ui.ExpectedConditions;
import org.openqa.selenium.support.ui.Select;
import org.openqa.selenium.support.ui.WebDriverWait;

import java.net.MalformedURLException;
import java.util.ArrayList;
import java.util.List;

public class SeleniumUtils {

    public static List<String> getAllSelectOption (WebElement element){
        Select select = new Select(element);
        List<String> option = new ArrayList<>();
        for (WebElement eachOption : select.getOptions()){
            option.add(eachOption.getText());
        }
        return option;

    }
    public static void waitForVisibility(WebElement element, int seconds) throws MalformedURLException {
        WebDriverWait wait = new WebDriverWait(Driver.getDriver(), seconds);
        wait.until(ExpectedConditions.visibilityOf(element));
    }

    public static void pause(int seconds) {
        try {
            Thread.sleep(seconds * 1000);
        } catch (InterruptedException e) {
            e.printStackTrace();
        }
    }

}
